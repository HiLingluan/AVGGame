namespace XLua.CSObjectWrap
{
    using Utils = XLua.Utils;
    public class ZFightWebUtilityWrap 
    {
        public static void __Register(RealStatePtr L)
        {
			ObjectTranslator translator = ObjectTranslatorPool.Instance.Find(L);
			System.Type type = typeof(ZFight.WebUtility);
			Utils.BeginObjectRegister(type, L, translator, 0, 0, 0, 0);
			
			
			
			
			
			
			Utils.EndObjectRegister(type, L, translator, null, null,
			    null, null, null);

		    Utils.BeginClassRegister(type, L, __CreateInstance, 3, 0, 0);
			Utils.RegisterFunc(L, Utils.CLS_IDX, "EscapeString", _m_EscapeString_xlua_st_);
            Utils.RegisterFunc(L, Utils.CLS_IDX, "UnescapeString", _m_UnescapeString_xlua_st_);
            
			
            
			
			
			
			Utils.EndClassRegister(type, L, translator);
        }
        
        [MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
        static int __CreateInstance(RealStatePtr L)
        {
            return LuaAPI.luaL_error(L, "ZFight.WebUtility does not have a constructor!");
        }
        
		
        
		
        
        
        
        
        [MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
        static int _m_EscapeString_xlua_st_(RealStatePtr L)
        {
		    try {
            
            
            
                
                {
                    string _stringToEscape = LuaAPI.lua_tostring(L, 1);
                    
                        var gen_ret = ZFight.WebUtility.EscapeString( _stringToEscape );
                        LuaAPI.lua_pushstring(L, gen_ret);
                    
                    
                    
                    return 1;
                }
                
            } catch(System.Exception gen_e) {
                return LuaAPI.luaL_error(L, "c# exception:" + gen_e);
            }
            
        }
        
        [MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
        static int _m_UnescapeString_xlua_st_(RealStatePtr L)
        {
		    try {
            
            
            
                
                {
                    string _stringToUnescape = LuaAPI.lua_tostring(L, 1);
                    
                        var gen_ret = ZFight.WebUtility.UnescapeString( _stringToUnescape );
                        LuaAPI.lua_pushstring(L, gen_ret);
                    
                    
                    
                    return 1;
                }
                
            } catch(System.Exception gen_e) {
                return LuaAPI.luaL_error(L, "c# exception:" + gen_e);
            }
            
        }
        
        
        
        
        
        
		
		
		
		
